    private boolean isNumeric(String s){
        try {
            int n = Integer.parseInt(s);
            return true;
        } catch (NumberFormatException e) {
            return false;
        }
    }
    
    private boolean isFecha(String f){
        try {
            LocalDate fecha = LocalDate.parse(f);
            return true;
        } catch (Exception e) {
            return false;
        }
    }
    
